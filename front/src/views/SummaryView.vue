        <h3>7. Figure out r/w JSON file</h3>
        <p>
          For the development of the editable wiki, I decided to use Express
          with
          <code>GET</code>and<code>PUT</code> requests from the client to the
          server implemented by Express. About the data storage method,
          read/write a data from/to the <code>JSON</code> file which is located
          server local was adopted as this is less expensive to develop than
          using a database. Using database is not mandatory requirement for this
          assessment.
        </p>
        <h4>Server side (back-end)</h4>
        <pre><code>
        /* index.js */
        const dataFile = "data.json";

        app.get("/info", (req, res) => {
          const data = JSON.parse(fs.readFileSync(dataFile, "utf8"));
          res.send(data);
        });

        app.put("/info", (req, res) => {
          let data = JSON.stringify({ contents: req.body.contents });
          fs.writeFileSync(dataFile, data);
          res.send(200);
        });
        </code></pre>
        <ul>
          <li>
            GET<code>/info</code>... read data from data.json, convert string to
            JSON object and send it
          </li>
          <li>
            PUT<code>/info</code>... convert JSON object to string and write it
            to data.json
          </li>
        </ul>
        <h4>Client side (front-end)</h4>
        <pre><code>
        methods: {
          getInfo: function () {
            fetch("/info", {
              method: "GET",
              headers: {
                Accept: "application/json",
                "Content-Type": "application/json",
              },
            })
              .then((response) => response.json())
              .then((data) => (this.contents = data.contents))
          },
          saveInfo: function (data) {
            fetch("/info", {
              method: "PUT",
              headers: {
                Accept: "application/json",
                "Content-Type": "application/json",
              },
              body: JSON.stringify(data),
            })
              .then(() => {
                this.getInfo();
              })
          },
        </code></pre>
        <ul>
          <li>
            <code>getInfo</code>... send GET request to server and assign data
            to a variable
          </li>
          <li>
            <code>saveInfo</code>... send PUT request with edited data, then
            send GET request to update data
          </li>
        </ul>
